#TERNARY OPERATOR :

     # it is a on line shortened for an if-else statement . 

     # SYNTAX :
        # value_if_true if condition else value_if_false

    # it is best when you need simple decision one line. 

    # avoid if the logic is too compelex . keep your code readable. 

# EXAMPLE :
        
age = 18
print("adult" if age>=18 else"minor")        


