# Membership operator :
    #  >> in
    #  >> not in

#>>>>>>>>>>>>> in

# in integer :

lst = [1,2,3,4,5]

print(5 in lst)
print(6 in lst)


# in string:
 
name = "vikas"
print("v" in name )
print("h" in name)

#>>>>>>>>>>>> not in
 
# in integer:

lst = [1,2,3,4,5]

print(6 not in lst)
print(5 not in lst)
 
# in string :

name = "vikas"

print("V" not in name)
print("v" not in name)

#>>>>>> QUIZZZ

numbers = [1,4,'2',8,5]

print(2 in numbers)
print(10 not in numbers)
